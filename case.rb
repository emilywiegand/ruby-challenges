weather = "sunny"

case weather
when "sunny"
puts "Put on those shades!"
when "cloudy"
puts "No need for sunscreen!"
when "rainy"
puts "No suede shoes today."
when "snowy"
puts "OH MY GOD!!! SNOWMEN!!!"
when "windy"
puts "Avoid flowing skirts."
when "humid"
puts "Yuck, ponytail day!"
when "dry"
puts "Time for some lotion!"
when "hot"
puts "Pool day!"
when "cold"
puts "Brrr, bundle up!"
else
puts "Who knows, be prepared!"
end
